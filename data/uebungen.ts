export function findeUebungById(id: string): Uebung | undefined {
  for (const bereich of BEREICHE) {
    const uebung = bereich.uebungen.find((u) => u.id === id);
    if (uebung) return uebung;
  }
  return undefined;
}

export function findeBereichById(id: string): Bereich | undefined {
  return BEREICHE.find((b) => b.id === id);
}
